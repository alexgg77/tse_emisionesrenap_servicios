package com.example.springsocial.model.input;

public class modelCambioCui {
	
	private String cui;
	private String serie;
	private String cuianterior;
	private String numerosolicitud;
	
	
	
	public modelCambioCui() {
		super();
	}
	public String getCui() {
		return cui;
	}
	public void setCui(String cui) {
		this.cui = cui;
	}
	public String getSerie() {
		return serie;
	}
	public void setSerie(String serie) {
		this.serie = serie;
	}
	public String getCuianterior() {
		return cuianterior;
	}
	public void setCuianterior(String cuianterior) {
		this.cuianterior = cuianterior;
	}
	public String getNumerosolicitud() {
		return numerosolicitud;
	}
	public void setNumerosolicitud(String numerosolicitud) {
		this.numerosolicitud = numerosolicitud;
	}

	
	
}
